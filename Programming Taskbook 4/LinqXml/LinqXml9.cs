using PT4;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.IO;
using System.Xml.Linq;

namespace PT4Tasks
{
    public class MyTask : PT
    {
        // При решении задач группы LinqXml доступны следующие
        // дополнительные методы расширения, определенные в задачнике:
        //
        //   Show() и Show(cmt) - отладочная печать последовательности,
        //     cmt - строковый комментарий;
        //
        //   Show(e => r) и Show(cmt, e => r) - отладочная печать
        //     значений r, полученных из элементов e последовательности,
        //     cmt - строковый комментарий.

        public static void Solve()
        {
            Task("LinqXml9");

            var sourceString = File.ReadAllLines(GetString(), Encoding.Default); // получаем XML строку

            XDocument stringProcessing = new XDocument( // конструктор класса XDocument
               new XDeclaration(null, "windows-1251", null),
               new XElement("root", // элемент 0 уровня
                   sourceString.Select(stringXML => stringXML.StartsWith("comment:") ? 
                   
                   new XComment(stringXML.Substring(8)) : // оборачивает в комментарий

                   new XElement("line", stringXML) as object)));

            /*
            XDocument stringProcessing = new XDocument(
                new XDeclaration(null, "windows-1251", null),
                new XElement("root",
                    sourceString.Select(stringXML => stringXML.StartsWith("comment:") ?
                    new XProcessingInstruction("d", stringXML.Substring(8)) : // Substring() вырезает кол. букв

                    new XElement("line", stringXML) as object)));
            */

            stringProcessing.Save(GetString());
        }
    }
}
